@Composable
fun SwipeHorizontallyScreen(){

    val width = 96*2.dp
    val squareSize =  48*2.dp

    Column {
        Spacer(Modifier.height(16.dp))
        SwipeableSample( width, squareSize)
        SwipeableSample3Anchors( squareSize*3 , squareSize )
        SwipeableSampleWider2Anchors(squareSize*4 , squareSize)
    }

}

@OptIn( ExperimentalWearMaterialApi::class)
@Composable
private fun SwipeableSample(
    width: Dp = 96.dp,
    squareSize: Dp = 48.dp,
) {

    val swipeableState = rememberSwipeableState(0)
    val sizePx = with(LocalDensity.current) { squareSize.toPx() }
    val anchors = mapOf(0f to 0, sizePx to 1) // Maps anchor points (in px) to states

    Box(
        modifier = Modifier
            .width(width)
            .swipeable(
                state = swipeableState,
                anchors = anchors,
                orientation = Orientation.Horizontal,
            )
            .background(Color.LightGray)
    ) {
        Box(
            Modifier
                .offset { IntOffset(swipeableState.offset.value.roundToInt(), 0) }
                .size(squareSize)
                .background(Color.DarkGray)
        )
    }
}

@OptIn( ExperimentalWearMaterialApi::class)
@Composable
private fun SwipeableSample3Anchors(
    width: Dp = 96.dp,
    squareSize: Dp = 48.dp
) {

    val swipeableState = rememberSwipeableState(0)
    val sizePx = with(LocalDensity.current) { squareSize.toPx() }
    val anchors = mapOf(0f to 0, sizePx to 1 , (sizePx*2) to 2) // Maps anchor points (in px) to states

    Box(
        modifier = Modifier
            .width(width)
            .swipeable(
                state = swipeableState,
                anchors = anchors,
                thresholds = { _, _ -> FractionalThreshold(0.3f) },
                orientation = Orientation.Horizontal
            )
            .background(Color.LightGray)
    ) {
        Box(
            Modifier
                .offset { IntOffset(swipeableState.offset.value.roundToInt(), 0) }
                .size(squareSize)
                .background(Color.DarkGray)
        )
    }
}

